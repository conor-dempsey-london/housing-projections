"""
Builds notebooks/8.0-cd-az3_estimates_dashboard.py -- a marimo notebook with
Phase A's CSVs + the borough boundary GeoJSON embedded directly as
gzip+base64 string literals in its own source, so it has ZERO runtime file
or private-package dependency (no housing_projections/gla_data/geopandas
import, no filesystem read). That's required for
`marimo export html-wasm` to produce a single, truly self-contained static
HTML file that non-technical stakeholders can open with no server and no
access to this repo's private dataset -- see
docs/estimates-dashboard-report-plan.md Phase B.

Re-run this whenever results/artifacts/az3_year_estimates/*.csv or
borough_boundaries.geojson change; it always overwrites the notebook file
from scratch (the notebook itself is generated output, not hand-edited).

Usage
-----
    pixi run python scripts/build_dashboard.py
"""
import base64
import gzip
from pathlib import Path

_REPO_ROOT = Path(__file__).resolve().parent.parent
DATA_DIR = _REPO_ROOT / 'results' / 'artifacts' / 'az3_year_estimates'
OUTPUT_PATH = _REPO_ROOT / 'notebooks' / '8.0-cd-az3_estimates_dashboard.py'


def _encode(path):
    raw = path.read_bytes()
    return base64.b64encode(gzip.compress(raw, compresslevel=9)).decode('ascii')


def main():
    blobs = {
        'AREA_YEAR_ESTIMATES_GZB64': _encode(DATA_DIR / 'area_year_estimates.csv'),
        'AREA_SCENARIOS_GZB64': _encode(DATA_DIR / 'area_scenarios.csv'),
        'AREA_TIER_SUMMARY_GZB64': _encode(DATA_DIR / 'area_tier_summary.csv'),
        'BOROUGH_LONDON_TOTALS_GZB64': _encode(DATA_DIR / 'borough_london_totals.csv'),
        'BOROUGH_BOUNDARIES_GZB64': _encode(DATA_DIR / 'borough_boundaries.geojson'),
    }
    for name, blob in blobs.items():
        print(f'{name}: {len(blob) / 1e3:.0f} KB (base64)')

    notebook_source = NOTEBOOK_TEMPLATE.format(**blobs)
    OUTPUT_PATH.write_text(notebook_source)
    print(f'\nWrote {OUTPUT_PATH} ({OUTPUT_PATH.stat().st_size / 1e6:.1f} MB)')


