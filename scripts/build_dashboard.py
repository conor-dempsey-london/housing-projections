@app.cell(hide_code=True)
def _(mo):
    mo.md(
        """
        ## Does uncertainty add up consistently across scales?

        A basic sanity check: if we (incorrectly) added up each individual LSOA's own
        uncertainty range one by one, would we get the same range the model actually reports
        for the borough or London total selected above? We shouldn't, and we don't --
        individual areas' timing uncertainty doesn't all point the same way at once, so the
        ranges should *not* simply add up. The model's actual borough/London-level range is
        genuinely narrower than that naive sum, which is exactly what should happen if
        uncertainty is being combined correctly across scales, from the same underlying
        LSOA-level figures, rather than estimated separately at each level.
        """
    )
    return


@app.cell(hide_code=True)
def _(area_tier_summary, area_year_estimates, borough_choice, pd):
    _yr = area_year_estimates.merge(area_tier_summary[["area", "borough_name"]], on="area")
    _yr["ci_width"] = _yr["z_hi90"] - _yr["z_lo90"]
    if borough_choice.value == "London":
        _naive = _yr.groupby("year")["ci_width"].sum()
    else:
        _naive = _yr[_yr["borough_name"] == borough_choice.value].groupby("year")["ci_width"].sum()
    consistency_check = pd.DataFrame({{"naive_sum_of_area_ranges": _naive}}).reset_index()
    return (consistency_check,)


@app.cell(hide_code=True)
def _(alt, borough_choice, borough_london_totals, consistency_check, mo):
    _actual = borough_london_totals[
        (borough_london_totals["geography"] == borough_choice.value)
        & (borough_london_totals["year"] != "total")
    ].copy()
    _actual["year"] = _actual["year"].astype(int)
    _actual["actual_range"] = _actual["z_total_hi90"] - _actual["z_total_lo90"]

    _cmp = consistency_check.merge(_actual[["year", "actual_range"]], on="year")
    _long = _cmp.melt(id_vars="year", var_name="series", value_name="range_width")
    _label_map = {{
        "naive_sum_of_area_ranges": "If you (wrongly) added up every area's own range",
        "actual_range": f"Actual {{borough_choice.value}} range (correct)",
    }}
    _long["series"] = _long["series"].map(_label_map)

    _chart = (
        alt.Chart(_long)
        .mark_bar()
        .encode(
            x=alt.X("year:O", title="Year"),
            y=alt.Y("range_width:Q", title="90% range width (dwellings)"),
            color=alt.Color("series:N", title=None),
            xOffset="series:N",
            tooltip=["year", "series", alt.Tooltip("range_width:Q", format=",.0f")],
        )
        .properties(
            width=650, height=280,
            title=f"{{borough_choice.value}}: naive vs. actual uncertainty range",
        )
    )

    _avg_ratio = (_cmp["actual_range"] / _cmp["naive_sum_of_area_ranges"]).mean()
    mo.vstack(
        [
            mo.ui.altair_chart(_chart),
            mo.md(
                f"*For {{borough_choice.value}}, the model's actual range is on average "
                f"**{{_avg_ratio:.0%}}** of what naively adding up every area's own range would "
                f"give -- uncertainty shrinks in relative terms as you aggregate, as it should.*"
            ),
        ]
    )
    return


